let score = 90;

if( score >= 90 ){
    console.log("Golden A+");
    
} else if ( score >= 80 ) {
    console.log("A+");
    
} else if( score >= 70 ) {
    console.log("A");
    
} else if ( score >= 60 ) {
    console.log("A-");
    
} else if ( score >= 50 ) {
    console.log("B");
    
} else if ( score >= 40 ) {
    console.log("C");
    
} else {
    console.log("F");
    
}