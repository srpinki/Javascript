
//Normal function
function sumNumber(num1, num2){
    return num1 + num2;
}

console.log(sumNumber(40, 20));


//Arrow function
let arrowFunction = (arrow1, arrow2) => {
    return arrow1 + arrow2;
}
console.log(arrowFunction(30, 40));

//Arrow function
const arrowFunction1 = (arrow3, arrow4) => arrow3 + arrow4;

